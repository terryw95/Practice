let buttonJacob = document.getElementById('button-jacob');
let jacob = document.getElementById('jacob');
let closeButtonJacob = document.getElementById('close-button-jacob');

function openButtonJacob () {
  buttonJacob.innerHTML = 'Jacob Torres';
  jacob.style.display = 'block';
  closeButtonJacob.style.display = 'block';
}

function closeItJacob () {
  buttonJacob.innerHTML = 'Leo XY';
  buttonJacob.style.cursor = 'pointer';
  jacob.style.display = 'none';
  closeButtonJacob.style.display = 'none';
}

buttonJacob.addEventListener('click', openButtonJacob);
closeButtonJacob.addEventListener('click', closeItJacob);

